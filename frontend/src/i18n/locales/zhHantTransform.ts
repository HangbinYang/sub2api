function translateString(value: string): string {
  let result = value

  for (const [source, target] of phraseReplacements) {
    result = result.split(source).join(target)
  }

  return Array.from(result, (character) => characterReplacements[character] ?? character).join('')
}

function transformLocaleValue<T extends LocaleValue>(value: T): T {
  if (typeof value === 'string') {
    return translateString(value) as T
  }

  if (Array.isArray(value)) {
    return value.map((item) => transformLocaleValue(item)) as T
  }

  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value).map(([key, child]) => [key, transformLocaleValue(child)])
    ) as T
  }

  return value
}

function applyPathOverride(target: Record<string, LocaleValue>, path: string, value: string): void {
  const segments = path.split('.')
  let current: Record<string, LocaleValue> | undefined = target

  for (let index = 0; index < segments.length - 1; index += 1) {
    const segment = segments[index]
    const next = current?.[segment]
    if (!next || typeof next !== 'object' || Array.isArray(next)) {
      return
    }
    current = next as Record<string, LocaleValue>
  }

  const lastSegment = segments[segments.length - 1]
  if (current && lastSegment in current) {
    current[lastSegment] = value
  }
}

export function createTaiwanTraditionalLocale<T extends Record<string, LocaleValue>>(source: T): T {
  const translated = transformLocaleValue(source)
  for (const [path, value] of Object.entries(manualOverrides)) {
    applyPathOverride(translated, path, value)
  }
  return translated
}
